package Model;
/**
*Această clasă servește drept model pentru stocarea și manipularea
 * informațiilor despre clienți în cadrul aplicației de gestionare a depozitului.
 */
public class Clients {
    public int id;
    public String name;
    public String email;
    public String address;

    public Clients() {
    }

    /**
     * Class constructor.
     */
    public Clients(int id, String name, String email, String address) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.address = address;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Clients{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", email='" + email + '\'' +
                ", address='" + address + '\'' +
                '}';
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }


}
